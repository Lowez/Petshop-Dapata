    private void btIncluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btIncluirActionPerformed
        Gravar_Arquivo gravar = new Gravar_Arquivo();
        String nome_arq = txNomeAnimal.getText() + txNome.getText();
        
        Banho banho = new Banho();
        banho.setCusto(Double.parseDouble(txCusto.getText()));
        banho.setDataAgendada(txData.getText());
        banho.setTipoShampoo(txShampoo.getText());
        banho.setNivelDeLimpeza(0);
        banho.setNomeEstagiario(txNome.getText());
        banho.setTipo_animal(txAnimal.getText());
        banho.setNome_animal(txNomeAnimal.getText());
        
        try {
            gravar.gravarArq(nome_arq, banho);
        } catch (IOException ex) {
            Logger.getLogger(BanhoAdicionar.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btIncluirActionPerformed

