    private void btIncluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btIncluirActionPerformed
        Gravar_Arquivo gravar = new Gravar_Arquivo();
        String nome_arq = (txNomeAnimal.getText() + txData.getText());
        
        Consulta consulta = new Consulta();
        consulta.setCusto(Double.parseDouble(txCusto.getText()));
        consulta.setDataAgendada(txData.getText());
        consulta.setNomeVeterinarioResponsavel(txNome.getText());
        consulta.setNivelDeSaude(0);
        consulta.setTipo_animal(txAnimal.getText());
        consulta.setNome_animal(txNomeAnimal.getText());
        
        try {
            gravar.gravarArq(nome_arq, consulta);
        } catch (IOException ex) {
            Logger.getLogger(ConsultasAdicionar.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btIncluirActionPerformed

